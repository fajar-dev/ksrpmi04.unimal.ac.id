                    <p> Walaupun KSR-PMI Unit 04 belum mampu memberiakan yang terbaik dan maksimal terhadap pelayanaan dan pertolongan kepada masyarakat, akan tetapi kontrbusi KSR Unimal telah begitu banyak menunjang terciptanya Iklim Kondusif dalam masyarakat. Dimana saat ini kondisi NAD, yang belum bangkit stabil dari musibah Tsunamai  yang merenggut ratusan ribu manusia dan kerugian material lainnya, kembali lagi banjir melanda beberapa Kecamatan dan Kabupaten di Aceh dan KSR-PMI Unit 04 Universitas Malikussaleh telah berdiri sebagai sukarelawan garis depan untuk membantu masyarakat dalam penanganannya, seperti Evakuasi korban yang terjebak banjir ketempat pengungsian, membantu konsumsi serta air bersih bagi pengungsi dan juga melakukan pengawasan kesehatan terhadap pengungsi. </p>
